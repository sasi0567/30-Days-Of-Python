# Checking data types

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(1 + 3j))              # Complex
print(type('Helloo'))          # String
print(type([1, 2, 3]))           # List
print(type({'Name': 'Sasidhar'}))  # Dictionary
print(type({1.2, 2.4, 3.14}))    # Tuple

